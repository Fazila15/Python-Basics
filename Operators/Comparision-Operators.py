# Comparision operators gives output in boolean ( True / False )

x = 10
y = 5

print(x == y)  # Checks if x is EQUALS to y
print(x != y)  # Checks if x is NOT EQUALS to y
print(x > y)   # Checks if x is GREATER THAN y
print(x < y)   # CHecks if x is LESS THAN y
print(x >= y)  # CHecks if x is GREATER THAN OR EQUALS TO y
print(x <= y)  # CHecks if x is LESS THAN OR EQUALS TO y
